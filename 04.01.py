l1= [1, 5, 6, 8, 2, 45, 65, 25, 64, 85, 67]
print(l1)

l1.sort()
print(l1)

l1.reverse()
print(l1)

l1.append(7) #add 7 at the end
print(l1)

l1.insert(3,10) # this will add 10 at 3rd index
print(l1)

l1.pop(3) #this will delet the 3rd index
print(l1)

l1.remove(7) #this will remove 7 from index
print(l1)